public class ex1 {

    public static void main (String[] args) {
        perimeter();
        area();
    }

    public static void perimeter(){

        int a=2;
        int b=6;

        int perimeter = 2 * a + 2*b;

        System.out.println(perimeter);
    }

    public static void area(){
        int a=2;
        int b=6;

        int area = a*b;

        System.out.println(area);
    }
}
